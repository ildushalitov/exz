public class Play {
    private int filmId;
    private int actorId;

    public int getFilmId() {
        return filmId;
    }

    public int getActorId() {
        return actorId;
    }

    public Play(int filmId, int actorId) {
        this.filmId = filmId;
        this.actorId = actorId;
    }
}
